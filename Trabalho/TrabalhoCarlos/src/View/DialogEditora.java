    private void tableEditoraMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableEditoraMouseClicked
        // TODO add your handling code here:

        try{
            if(evt.getClickCount()==2){
                String valor = tableEditora.getValueAt(tableEditora.getSelectedRow(), 0)+"";
                int codigo = Integer.parseInt(valor);
                populateComponente(dao.getEditora(codigo));
            }
        }catch(SQLException ex){
            System.out.println("Erro: " + ex.getMessage());
        }
    }//GEN-LAST:event_tableEditoraMouseClicked

