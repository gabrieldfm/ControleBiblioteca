    private void carregaCidades() throws SQLException{
        DefaultComboBoxModel cbm = new DefaultComboBoxModel(
        new Vector(new DaoCidade().getCidadeList()));
        comboCidade.setModel(cbm);
    }
    
    private void carregaEditoras() throws SQLException{
        List<Editora> lista = dao.getEditoraList();
        tableEditora.setModel(
            new MyTableModel(Padrao.class, lista, tableEditora)
        );
    }
    
    private Editora populateObject(){
        return (new Editora(
                    (Cidade)comboCidade.getSelectedItem(),
                    textId.getText().isEmpty()?0:Integer.parseInt(textId.getText()),
                    textNome.getText()));
    }
    
    private void iniciaComponentes(){
        textId.setText("");
        textNome.setText("");
        comboCidade.setSelectedIndex(0);
        textFiltro.setText("");
        textNome.requestFocus();
    }
    
    private void carregaEditoras(String filtro) throws SQLException{
        List<Editora> lista = dao.getEditoraList(filtro);
        tableEditora.setModel(
            new MyTableModel(Padrao.class, lista, tableEditora)
        );    
    }
    
    private void populateComponente(Editora editora){
        textId.setText(editora.getId()+"");
        textNome.setText(editora.getNome());
        comboCidade.setSelectedItem(editora.getCidade());
    } 
    
